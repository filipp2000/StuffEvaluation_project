    private void jTable_EvaluationsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_EvaluationsMouseClicked
        DefaultTableModel model = (DefaultTableModel)jTable_Evaluations.getModel();

        int selectedRow = jTable_Evaluations.getSelectedRow();
        int selectedCol = jTable_Evaluations.getSelectedColumn();             
        
        String subm_date = jTable_Evaluations.getValueAt(selectedRow, 9).toString();       
        String curr_date = java.time.LocalDate.now().toString();
        
        
        if (selectedCol == 5 ) // interview column selected
        {
            String column_value = (String) jTable_Evaluations.getValueAt(selectedRow, selectedCol);
            String ev_id = jTable_Evaluations.getValueAt(selectedRow, 0).toString();             
            
            if(curr_date.compareTo(subm_date) > 0 && column_value == null) {
                
                String[] options = {"0", "1", "2", "3", "4"};
                String grade = (String) JOptionPane.showInputDialog(null, "Interview", "Select grade(0-4):"
                    ,JOptionPane.QUESTION_MESSAGE,null, options, options[0]);
                
                if(grade != null){                   
                    setInterview(ev_id, grade);
                    model.setValueAt(grade, selectedRow, selectedCol);
                    finalizeEvaluationResult(selectedRow);                
                }                       
            }
            else if(curr_date.compareTo(subm_date) < 0 && column_value == null) 
                JOptionPane.showMessageDialog(null, "Submission date not expired yet!");       
        }
        
        if (selectedCol == 6) // report column selected
        {
            String column_value = (String) jTable_Evaluations.getValueAt(selectedRow, selectedCol);
            String ev_id = jTable_Evaluations.getValueAt(selectedRow, 0).toString();              
            
            if(curr_date.compareTo(subm_date) > 0 && column_value == null) {
               
                String[] options = {"0", "1", "2", "3", "4"};
                String grade = (String) JOptionPane.showInputDialog(null, "Report", "Select grade(0-4):"
                    ,JOptionPane.QUESTION_MESSAGE,null, options, options[0]);
                
                if(grade != null){                   
                    setReport(ev_id, grade);
                    model.setValueAt(grade, selectedRow, selectedCol);
                    finalizeEvaluationResult(selectedRow);
                }
            }
            else if(curr_date.compareTo(subm_date) < 0 && column_value == null) 
                JOptionPane.showMessageDialog(null, "Submission date not expired yet!");        
        }
        
        if (selectedCol == 7 ) // extras column selected
        {
            String column_value = (String) jTable_Evaluations.getValueAt(selectedRow, selectedCol);
            String ev_id = jTable_Evaluations.getValueAt(selectedRow, 0).toString();       
       
            if(curr_date.compareTo(subm_date) > 0 && column_value == null) {
                
                String[] options = {"0", "1", "2"};
                String grade =  (String) JOptionPane.showInputDialog(null, "Extras", "Select grade(0-2):"
                    ,JOptionPane.QUESTION_MESSAGE,null, options, options[0]);

                if(grade != null){
                    setExtras(ev_id, grade);
                    model.setValueAt(grade, selectedRow, selectedCol);
                    finalizeEvaluationResult(selectedRow);
                }
            }
            else if(curr_date.compareTo(subm_date) < 0 && column_value == null) 
                JOptionPane.showMessageDialog(null, "Submission date not expired yet!");      
        }
       
    }//GEN-LAST:event_jTable_EvaluationsMouseClicked

    
     private void setInterview(String ev_id, String interv_grade){
        
        CallableStatement cs;
        
        String proc = "{CALL setInterview_grade(?,?)}";    
        
        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);
            
            cs.setString(1, ev_id);
            cs.setString(2, interv_grade);
            
            cs.execute();
            JOptionPane.showMessageDialog(null, "Interview grade added!");
        
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }
    
    
