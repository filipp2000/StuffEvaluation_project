    private void finalizeEvaluationResult(int selectedRow){
        
        String interview = (String) jTable_Evaluations.getValueAt(selectedRow, 5);
        String report = (String) jTable_Evaluations.getValueAt(selectedRow, 6);
        String extras = (String) jTable_Evaluations.getValueAt(selectedRow, 7);
        String jod_id = jTable_Evaluations.getValueAt(selectedRow, 3).toString();
       
        String evaluator = MenuEvaluator.EvaluatorUsername;
                
        CallableStatement cs;
        
        String proc = "{CALL finalizeEval_3_2(?,?,?,?,?)}";    
        
        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);
            
            cs.setString(1, evaluator);
            cs.setString(2, jod_id);
            cs.setString(3, interview);
            cs.setString(4, report);
            cs.setString(5, extras);
            
            
            cs.execute();
            JOptionPane.showMessageDialog(null, "Evaluation finalized!");
        
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
                
    
    }
    
    private void showEvaluationApplics(){
        
        CallableStatement cs;
        ResultSet res;

        String proc = "{CALL view_evalsApplics(?)}";

        String username = MenuEvaluator.EvaluatorUsername;

        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);

            cs.setString(1, username);
            
            res = cs.executeQuery();
            jTable_Evaluations.setModel(DbUtils.resultSetToTableModel(res));


        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }
   
    
    
