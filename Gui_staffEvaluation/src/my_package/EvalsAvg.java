    private void jTable_EvalsAvgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_EvalsAvgMouseClicked
        int selectedRow = jTable_EvalsAvg.getSelectedRow();
        
        CallableStatement cs;
        ResultSet res;
        
        String proc = "{CALL get_evalGradeAvg(?)}";        
              
        String evaluator = jTable_EvalsAvg.getValueAt(selectedRow, 0).toString();
       
        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);
            
            cs.setString(1, evaluator);
            
            res = cs.executeQuery();
        
            JTable evalTable = new JTable();
            evalTable.setModel(DbUtils.resultSetToTableModel(res));
            JOptionPane.showMessageDialog(null, evalTable, "Average Evaluation Grade", JOptionPane.INFORMATION_MESSAGE);
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }//GEN-LAST:event_jTable_EvalsAvgMouseClicked

    private void showEvalAvg(){
        CallableStatement cs;
        ResultSet res;
        
        String proc = "{CALL show_evaluator(?)}";        
        String username = MenuManager.ManagerUsername;        
       
        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);
            
            cs.setString(1, username);
            
            res = cs.executeQuery();
            jTable_EvalsAvg.setModel(DbUtils.resultSetToTableModel(res));
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }
   
