    private void showCompJobs(){       
        CallableStatement cs;
        ResultSet res;

        String proc = "{CALL view_compJobs(?)}";

        String username = MenuEvaluator.EvaluatorUsername;

        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);

            cs.setString(1, username);
            
            res = cs.executeQuery();
            jTable_JobApplic.setModel(DbUtils.resultSetToTableModel(res));


        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }
    
