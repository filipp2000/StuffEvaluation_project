    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed

        int i = jTable_JobApplic.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable_JobApplic.getModel();

        String username = model.getValueAt(i, 5).toString();
        String _id = model.getValueAt(i, 0).toString();
        String startDate = jTextField_StartDate.getText();
        String salary = jTextField_Salary.getText();
        String pos = jTextField_Position.getText();
        String edra = jTextField_Edra.getText();
        String submDate = jTextField_SubmDate.getText();
        
        CallableStatement cs;

        String proc = "{CALL edit_evalJobs(?,?,?,?,?,?,?)}";

        try {
            cs = MySQLconnection.connectDb().prepareCall(proc);
            
            if(!username.equals(MenuEvaluator.EvaluatorUsername)){
                throw new Exception("Edit only the jobs announced by you ("+ MenuEvaluator.EvaluatorUsername+")");
            }
            
            cs.setString(1, username);
            cs.setString(2, _id);
            cs.setString(3, startDate);
            cs.setString(4, salary);
            cs.setString(5, pos);
            cs.setString(6, edra);
            cs.setString(7, submDate);


            cs.execute();
            JOptionPane.showMessageDialog(null, "Data Updated!");

            if(i>=0) {    
                model.setValueAt(startDate, i, 1);
                model.setValueAt(salary, i, 2);
                model.setValueAt(pos, i, 3);
                model.setValueAt(edra, i, 4);
                model.setValueAt(submDate, i, 8);
            }else{
                JOptionPane.showMessageDialog(null, "Error");
            } 
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

